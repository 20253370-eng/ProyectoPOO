    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        int dni = Integer.parseInt(txtDNI.getText());

String nombres = txtNombres.getText();
String apellidos = txtApellidos.getText();
String direccion = txtDireccion.getText();
int telefono = Integer.parseInt(txtTelefono.getText());


Cliente cliente = new Cliente(
        direccion,
        telefono,
        nombres,
        apellidos,
        dni
);


gestionClientes.registrarCliente(cliente);


modeloTabla.addRow(new Object[]{
    dni,
    nombres,
    apellidos,
    direccion,
    telefono
});


javax.swing.JOptionPane.showMessageDialog(this,
        "Cliente registrado correctamente");
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        int dni = Integer.parseInt(txtDNI.getText());

Cliente cliente = gestionClientes.buscarCliente(dni);

if(cliente != null){

    txtNombres.setText(cliente.getNombres());
    txtApellidos.setText(cliente.getApellidos());
    txtDireccion.setText(cliente.getDireccion());
    txtTelefono.setText(
            String.valueOf(cliente.getTelefono())
    );

}else{

    javax.swing.JOptionPane.showMessageDialog(this,
            "Cliente no encontrado");
}
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void tablaClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaClientesMouseClicked
        // TODO add your handling code here:
        int fila = tablaClientes.getSelectedRow();

txtDNI.setText(
        tablaClientes.getValueAt(fila, 0).toString()
);

txtNombres.setText(
        tablaClientes.getValueAt(fila, 1).toString()
);

txtApellidos.setText(
        tablaClientes.getValueAt(fila, 2).toString()
);

txtDireccion.setText(
        tablaClientes.getValueAt(fila, 3).toString()
);

txtTelefono.setText(
        tablaClientes.getValueAt(fila, 4).toString()
);
    }//GEN-LAST:event_tablaClientesMouseClicked

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        int dni = Integer.parseInt(txtDNI.getText());


boolean modificado = gestionClientes.modificarCliente(
        dni,
        txtNombres.getText(),
        txtApellidos.getText(),
        txtDireccion.getText(),
        Integer.parseInt(txtTelefono.getText())
);


if(modificado){

    int fila = tablaClientes.getSelectedRow();

    modeloTabla.setValueAt(txtDNI.getText(), fila, 0);
    modeloTabla.setValueAt(txtNombres.getText(), fila, 1);
    modeloTabla.setValueAt(txtApellidos.getText(), fila, 2);
    modeloTabla.setValueAt(txtDireccion.getText(), fila, 3);
    modeloTabla.setValueAt(txtTelefono.getText(), fila, 4);


    javax.swing.JOptionPane.showMessageDialog(this,
            "Cliente modificado correctamente");

}else{

    javax.swing.JOptionPane.showMessageDialog(this,
            "Cliente no encontrado");
}
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int fila = tablaClientes.getSelectedRow();

if(fila >= 0){

    int dni = Integer.parseInt(
            tablaClientes.getValueAt(fila, 0).toString()
    );


    boolean eliminado = gestionClientes.eliminarCliente(dni);


    if(eliminado){

        modeloTabla.removeRow(fila);

        javax.swing.JOptionPane.showMessageDialog(this,
                "Cliente eliminado correctamente");

    }else{

        javax.swing.JOptionPane.showMessageDialog(this,
                "Cliente no encontrado");
    }


}else{

    javax.swing.JOptionPane.showMessageDialog(this,
            "Seleccione un cliente");
}
    }//GEN-LAST:event_btnEliminarActionPerformed

