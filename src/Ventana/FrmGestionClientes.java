    private void cargarClientes(){

    modeloTabla.setRowCount(0);

    for(int i = 0; i < gestionClientes.getCantClientes(); i++){

        Cliente cli = gestionClientes.getListaClientes()[i];

        modeloTabla.addRow(new Object[]{
            cli.getDni(),
            cli.getNombres(),
            cli.getApellidos(),
            cli.getDireccion(),
            cli.getTelefono()
        });
    }
}
