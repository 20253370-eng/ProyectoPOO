    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:
        int dni = Integer.parseInt(txtDNI.getText());


Cliente cliente = gestionClientes.buscarCliente(dni);


if(cliente != null){


    int numero = gestionPedidos.generarNumeroPedido();


    String tipoEntrega = cmbTipoEntrega.getSelectedItem().toString();



    Pedido pedido = new Pedido();
    


    pedido.setNumero(numero);

    pedido.setCliente(cliente);

    pedido.setTipoEntrega(tipoEntrega);

    pedido.setEstado("En Cocina");


    pedido.setDetalles(detallesActuales);



    if(gestionPedidos.crearPedido(pedido)){


        javax.swing.JOptionPane.showMessageDialog(this,
                "Pedido creado correctamente");


        txtNumero.setText(String.valueOf(numero));
        cargarPedidos();

        cantDetalles = 0;
         

    }



}else{


    javax.swing.JOptionPane.showMessageDialog(this,
            "Cliente no encontrado");

}

    }//GEN-LAST:event_btnCrearActionPerformed

