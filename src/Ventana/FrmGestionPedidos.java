private void cargarProductos(){

    modeloProductos.setRowCount(0);

    for(int i = 0; i < gestionMenu.getCantProductos(); i++){

        Producto p = gestionMenu.getListaProductos()[i];

        modeloProductos.addRow(new Object[]{
            p.getCodigo(),
            p.getNombre(),
            p.getPrecioBase()
        });
    }
}
private void cargarCombos(){

    modeloCombos.setRowCount(0);

    for(int i = 0; i < gestionMenu.getCantCombos(); i++){

        Combo c = gestionMenu.getListaCombos()[i];

        modeloCombos.addRow(new Object[]{
            c.getCodigo(),
            c.getNombre(),
            c.getPrecioPromocional()
        });
    }
}

private void cargarPedidos(){

    modeloPedidos.setRowCount(0);

    for(int i = 0; i < gestionPedidos.getCantPedidos(); i++){

        Pedido p = gestionPedidos.getListaPedidos()[i];

        modeloPedidos.addRow(new Object[]{
            p.getNumero(),
            p.getCliente().getNombres(),
            p.getTipoEntrega(),
            p.getEstado()
        });
    }
}
